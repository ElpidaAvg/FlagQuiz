def greece():
    t.speed(5)
    t.penup()
    t.goto(-180, 120)
    t.pendown()

    stripe_height = 20
    for i in range(9):
        t.begin_fill()
        if i % 2 == 0:
            t.fillcolor("blue")
        else:
            t.fillcolor("white")
        for _ in range(2):
            t.forward(320)
            t.right(90)
            t.forward(stripe_height)
            t.right(90)
        t.end_fill()
        t.penup()
        t.right(90)
        t.forward(stripe_height)
        t.left(90)
        t.pendown()

    t.penup()
    t.goto(-180, 120)
    t.pendown()
    t.begin_fill()
    t.fillcolor("blue")
    for _ in range(2):
        t.forward(120)
        t.right(90)
        t.forward(80)
        t.right(90)
    t.end_fill()

    t.color("white")
    t.penup()
    t.goto(-180 + 60, 120)
    t.setheading(-90)
    t.pendown()
    t.pensize(8)
    t.forward(80)

    t.penup()
    t.goto(-180, 120 - 40)
    t.setheading(0)
    t.pendown()
    t.forward(120)

