guala = [austria, belgium, estonia, france, germany, italy, poland, sweden, greece, netherlands]

while len(guala) != 0 and health >= 1:
    t.reset() # katharizei tin othoni, epistrefei tin xelona sto kentro (0,0) kai koitaei pros ta deksia
    flag = random.choice(guala) # mia tyxaia epilogi apo ti lista guala
    flag()
    answer = input("Which flag is this? ")
    if answer.lower() == flag.__name__: # .lower() -> kanei mikra ta grammata tis synartiseis, __name__ -> pairnei to onoma tis synartiseis
        print("That's right!")
        points += 1
        print("Points:", points)
        guala.remove(flag) # vgazei ti shmaia apo ti guala an apantisw swsta
    else:
        health -= 1
        print("Wrong!")
        print("Lives:", health)

    if health == 0:
        t.reset()
        t.color("red")
        t.write("You lost!", align = "center", font = ("Tahoma", 30, "bold"))
